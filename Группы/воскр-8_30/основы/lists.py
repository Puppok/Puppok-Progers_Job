# Получение части массива
# array[start:] - вернет все значения начиная с индекса start
print(months[6:], end=' ')
print()

# array[:end] - вернет все значения до индекса end-1
print(months[:2], end=' ')
print()

# array[start:end] - вернет все значения с индекса start до end-1
print(months[4:6], end=' ')
print()

# array[start:end:step] - вернет все значения с индекса start до end-1 с шагом step 
print(months[1::3], end=' ')
print()

# Методы массивов
