# Перебор массива
# Перебор массива делается всегда с помощью цикла

    # с помощью цикла while
i = 0
while i < len(numbers):
    print(numbers[i] ** 3, end = ' ')
    i += 1
print()

    # с помощью цикла for
for i in numbers:
    print(i, end = ' ')
