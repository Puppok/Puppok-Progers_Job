# Арифметические операторы
# + - * / % ** //
num1 = int(input('Введите первое число: '))
num2 = int(input('Введите второе число: '))

print(f"{num1} + {num2} = {num1 + num2}") # сложение
print(f"{num1} - {num2} = {num1 - num2}") # вычитание
print(f"{num1} * {num2} = {num1 * num2}") # умножение
print(f"{num1} / {num2} = {num1 / num2}") # деление
print(f"{num1} % {num2} = {num1 % num2}") # остаток от деления
print(f"{num1} ** {num2} = {num1 ** num2}") # возведение в степень
print(f"{num1} // {num2} = {num1 // num2}") # целочисленное деление

# Операторы сравнения
# == != > < >= <=
print(f"{num1} == {num2} = {num1 == num2}") # равно
print(f"{num1} != {num2} = {num1 != num2}") # не равно
print(f"{num1} > {num2} = {num1 > num2}") # больше
print(f"{num1} < {num2} = {num1 < num2}") # меньше
print(f"{num1} >= {num2} = {num1 >= num2}") # больше или равно
print(f"{num1} <= {num2} = {num1 <= num2}") # меньше или равно

